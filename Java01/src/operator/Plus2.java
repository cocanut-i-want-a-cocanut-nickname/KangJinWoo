package operator;

public class Plus2 {

	public static void main(String[] args) {
		//숫자를 더하rl
		System.out.println(1+1);
		
		int 숫자1 = 1;
		int 숫자2 = 2;
		
		System.out.println(숫자1+숫자2);
		
		//빼기
		System.out.println(1-1);
		
		//곱하기
		System.out.println(2*2);
		
		//나누기
		System.out.println(4/2);
		
		//나누기 (나머지)
		System.out.println(5%2);
		
		int 숫자3 = 숫자1 + 숫자2;
		System.out.println(숫자3);
		
		//연산자도 우선순위가 있음.
	}

}
